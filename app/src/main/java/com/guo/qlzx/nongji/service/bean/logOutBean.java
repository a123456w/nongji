package com.guo.qlzx.nongji.service.bean;

/**
 * 退出登录
 */
public class logOutBean {

    /**
     * code : 0
     * message : 退出成功
     */

    private int code;
    private String message;

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }
}
